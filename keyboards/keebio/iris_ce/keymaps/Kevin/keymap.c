bool process_record_user(uint16_t keycode, keyrecord_t *record) {
   switch (keycode) {
      case MODLYR:
         if (record->event.pressed) {
            layer_on(_MODLYR);
         } else {
            layer_off(_MODLYR);
         }
         return false;
      case _MODLYR2:
         if (record->event.pressed) {
            layer_on(_MODLYR2);
         } else {
            layer_off(_MODLYR2);
         }
         return false;
      case QWERTY:
         if (record->event.pressed) {
            set_single_persistent_default_layer(_QWERTY);
         }
         return false;
      case GAME:
         if (record->event.pressed) {
            set_single_persistent_default_layer(_GAME);
         }
         return false;
      case MACOS:
         if (record->event.pressed) {
            set_single_persistent_default_layer(_MACOS);
         }
         return false;
      case MACRO1:
         if (record->event.pressed) {
            SEND_STRING("Macro 1 down");
         } else {
            SEND_STRING("Macro 1 up");
         }
         return false;
      case MACRO2:
         if (record->event.pressed) {
            SEND_STRING("Macro 2 down");
         } else {
            SEND_STRING("Macro 2 up");
         }
         return false;
      case MACRO3:
         if (record->event.pressed) {
            SEND_STRING("Macro 3 down");
         } else {
            SEND_STRING("Macro 4 up");
         }
         return false;
      default:
         return true;
   }
   return true;
}